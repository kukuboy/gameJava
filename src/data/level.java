package data;

public class level {
    int level;
    String bright;
    String dark;
    public void setLevel(int level){
        this.level=level;
    }
    public int getLevel(){
        return level;
    }
    public void setBright(String bright){
        this.bright=bright;
    }
    public String getBright(){
        return bright;
    }
    public void setDark(String dark){
        this.dark=dark;
    }
    public String getDark(){
        return dark;
    }
}
