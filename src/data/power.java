package data;

public class power {
    int level;
    int id;
    int own;
    int money;
    int bri;
    int p;
    int s;
    int t;

    public void setLevel(int level) {
        this.level = level;
    }

    public int getLevel() {
        return level;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setOwn(int own) {
        this.own = own;
    }

    public int getOwn() {
        return own;
    }

    public void setMoney(int money) {
        this.money = money;
    }

    public int getMoney() {
        return money;
    }

    public void setBri(int bri) {
        this.bri = bri;
    }

    public int getBri() {
        return bri;
    }

    public void setP(int p) {
        this.p = p;
    }

    public int getP() {
        return p;
    }

    public void setS(int s) {
        this.s = s;
    }

    public int getS() {
        return s;
    }

    public void setT(int t) {
        this.t = t;
    }

    public int getT() {
        return t;
    }
}
